// https://leetcode.com/problems/valid-sudoku/
/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function (board) {
  const set = new Set();
  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      const number = `${board[i][j]}`;
      if (number === ".") continue;
      const row = `${number} in row ${i}`;
      const col = `${number} in col ${j}`;
      const cellBlock = `${Math.trunc(i / 3)}-${Math.trunc(j / 3)}`;
      const block = `${number} in block ${cellBlock}`;
      if (set.has(row) || set.has(col) || set.has(block)) return false;
      set.add(row);
      set.add(col);
      set.add(block);
    }
  }
  return true;
};

